package Chpater01;

public class DoubleType07 {

	public static void main(String[] args) {
		// 정수: bit->byte(bet 8)->short->int->long
		//실수: float(4byte)-> double(8byte)
		
		float fnum=3.14f;//4byte  반드시 f붙이기
		double dnum=314;//8byte
		
		System.out.println(fnum);
		System.out.println(dnum);

	}

}
