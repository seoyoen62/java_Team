package Chpater07;

public class Car {
	String color;
	int cc;
	public Car(String color, int cc) {
		super();
		this.color = color;
		this.cc = cc;
	}
	public String getColor() {
		return color;
	}
	public void setColor(String color) {
		this.color = color;
	}
	public int getCc() {
		return cc;
	}
	public void setCc(int cc) {
		this.cc = cc;
	}
	

}
