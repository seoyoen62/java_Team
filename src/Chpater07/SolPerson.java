package Chpater07;

public class SolPerson {
	
	String name;
	float height;
	float weight;
	
	 public SolPerson() {
		
	}
	 
	 public SolPerson(String name) {
			super();
			this.name = name;
		}
	 
	public SolPerson(String name, float height, float weight) {
		super();
		this.name = name;
		this.height = height;
		this.weight = weight;
	}


	
}
