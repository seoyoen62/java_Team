package Chpater03.fortest;

public class ForTest07 {

	public static void main(String[] args) {
		
		for(int i=2; i<10;i++) {
			for(int j=1;j<10;j++) {
				System.out.println(i+"x"+j+"="+(i*j));
			}//in for
			System.out.println("==============================");
		}//out for

	}

}
